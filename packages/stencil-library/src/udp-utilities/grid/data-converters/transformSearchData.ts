export const transformSearchData = (searchData, diffCheckPayload) => {
  const transformed = {
    filterElements: [],
    filterGroups: []
  };

  function isDifferent(obj1, obj2, key) {
    if (obj1[key] === obj2[key]) {
      return false;
    }
    if (typeof obj1[key] === 'object' && typeof obj2[key] === 'object') {
      return !Object.keys(obj1[key]).every(subKey => isDifferent(obj1[key], obj2[key], subKey));
    }
    return true;
  }

  function transformKey(key) {
    const keyMappings = {

      'CountriesSelectedText': 'Country',
      'PostalCode': 'ZipPostalCode',
      'ProvincesSelectedText': 'ProvinceState',
      'RatingSelectedText': 'Rating',
      'MarketSrcSelectedText': 'MarketingSource',
      'Ownership':'[Ownership]',
      'ClientCompany': 'CLI.companyname',
      'TotalCompensation':'CLI.TotalCompensation',
      'BaseSalaryID':'',
      'LastSalaryIncrease':'CLI.LastSalaryIncrease',
      'CandidateOfflimitData': 'CTE_CandidateOfflimitData',
      'LastContactedDateFrom': '',
      'LastContactedDateTo': '',
      'YearsExpFrom': 'YearsOfExperience',
      'YearsExpTo': 'YearsOfExperience',
      'BirthSelectedDate':'age',
      'OverTimePayRateFrom': 'OverTimePayRate',
      'OverTimePayRateTo': 'OverTimePayRate',
      'MinimumPayRateFrom': 'MinimumPayRate',
      'MinimumPayRateTo': 'MinimumPayRate',
      'OwnershipText': 'Ownership',
      'PhoneNumber': 'BusinessPhone',

      // Job Order mapping
      'JobOrderID' : 'JobID',
      'EmploymentTypeSelectedValue': '',
      'EmploymentTypeSelectedText': 'EmploymentType',
      'DivisionSelectedText': 'Division',
      'CompanyName': 'CompanyName',
      'PositionSelectedText': 'JobTitle',
      'StatusSelectedText': 'Status',
      'StartDate': '',
      'ConditionStartDate': '',
      'EndDate': '',
      'ConditionEndDate': '',
      'ReqID': 'ReqId',
      'ReqStatusSelectedText': 'ReqStatusId',
      'DepartmentSelectedText': 'Department',
      'OfficeSelectedText': 'Office',
      'BusinessUnitSelectedText': 'BusinessUnit',
      'AutoOwnerSelectedText': 'Ownership',
      'AutoActMgrSelectedText': 'AccountManager',
      'CustomUserList1Text': 'CustomUserList1',
      'CustomUserList2Text': 'CustomUserList2',
      'CustomUserList3Text': 'CustomUserList3',
      'CountrySelectedText': 'Country',
      'ListCitiesValue': 'City',
      'ProvinceStateSelectedText': 'ProvinceState',
      'Address': 'Location',
      'ZipPostalCode': 'PostalZip',
      'ContactsFirstName': 'JobOrder.Contact.ContactPerson.FirstName',
      'ContactsLastName': 'JobOrder.Contact.ContactPerson.LastName',
       
      // Client mapping
      'ClientID': 'ClientID',
      'WebAddress': 'WebAddress',
      'Email': 'DefaultEmail',
      'IndustryTypeSelectedText': 'IndustryType',
      'DiscountStructureSelectedText': 'DiscountStructure'

    };
    return keyMappings[key] || key;
  }

  const optionalGroupIds = {};
  const addedFields = new Set();

  const createFilterElement = (searchField, searchOperator, searchValue, groupId = null) => ({
    searchField,
    searchOperator,
    searchValue,
    ...(groupId !== null && { groupId })
  });



  Object.keys(searchData).forEach(key => {
    if (!isDifferent(searchData, diffCheckPayload, key)) {
      return;
    }
  
    // Skip adding ConditionAvailabilityRelation as a separate field
    if (key === "ConditionAvailiabilityRelation") {
      return;
    }
    if (key === "StatusSelectedValue") {
      return;
    }

    if (key === "ProvincesSelectedValue") {
      return;
    }

    if (key === "MarketSrcSelectedValue") {
      return;
    }


    if (key === "OwnershipValue") {
      return;
    }

    
  
    let operator = "="; // Default operator

    if (key === "LastContactedDateFrom") {
      operator = ">=";
    } else if (key === "LastContactedDateTo") {
      operator = "<=";
    }


    if (key === "YearsExpFrom") {
      operator = ">=";
    } else if (key === "YearsExpTo") {
      operator = "<=";
    }


    if (key === "OvertimePayRateFrom") {
      operator = ">=";
    } else if (key === "OvertimePayRateTo") {
      operator = "<=";
    }

    if (key === "MinimumPayRateFrom") {
      operator = ">=";
    } else if (key === "MinimumPayRateTo") {
      operator = "<=";
    }


    if (key === "ConditionBaseSalaryRelation") {
      if (searchData[key] === "=") {
        operator = "=";
      } else if (searchData[key] === ">") {
        operator = ">";
      } else if (searchData[key] === "<") {
        operator = "<";
      } else if (searchData[key] === ">=") {   
        operator = ">=";
      } else if (searchData[key] === "<=") {
        operator = "<=";
      } else if (searchData[key] === "!=") {
        operator = "!=";
      }    
    }
      


   if (key === 'KeyWordsearch') {
      const queryResult = createStructuredQuery({ KeyWordsearch: searchData[key] });
      if (queryResult) {
        transformed.filterElements.push(...queryResult.filterElements);
        transformed.filterGroups = queryResult.filterGroups;
      }
    } else if (key.startsWith('Is') && key.endsWith('Optional')) {
      console.log('process optional key here...');
      const baseKey = key.substring(2, key.length - 8);
      console.log(`Processing 'Is...Optional' key: ${key}, BaseKey: ${baseKey}`);
      if (baseKey in searchData) {
        const groupId = optionalGroupIds[baseKey] || (Object.keys(optionalGroupIds).length + 1);
        optionalGroupIds[baseKey] = groupId;
        console.log(`Assigned groupId ${groupId} to baseKey: ${baseKey}`);
        if (!transformed.filterGroups.some(group => group.groupId === groupId)) {
          transformed.filterGroups.push({
            groupId: groupId,
            groupOperator: 2, // OR operator
            parentGroupId: null
          });
          // console.log(`Added new group for groupId: ${groupId}`);
        }
      }
    } else if (key === "AvailabilityOn") {
      // Use the value from ConditionAvailabilityRelation as the operator for AvailabilityOn
      
      const availabilityOperator = searchData['ConditionAvailiabilityRelation'];
      console.log('key meets AvailabilityOn ', searchData)
      transformed.filterElements.push(createFilterElement("Candidate.AvailableDate", availabilityOperator, searchData[key]));
    } else if (!addedFields.has(key)) {
      const groupId = optionalGroupIds[key];
      console.log('optional group ids',  optionalGroupIds)
      console.log('optional group ids keys',  key)
      console.log('optional group ids request',  optionalGroupIds[key])
      console.log('optional group id',  groupId)
      if (groupId) {
        addedFields.add(key);
      }
      transformed.filterElements.push(createFilterElement(key, operator, searchData[key], groupId));
    }
  });

  console.log('Before setting keys:', transformed.filterElements);

  // Function to rename keys in the final step
  function renameKeys(filterElement) {
    return {
      ...filterElement,
      searchField: transformKey(filterElement.searchField)
    };
  }

  console.log('Before renaming keys:', transformed.filterElements);

 

  // Update filterElements with correct groupIds
  transformed.filterElements.forEach(element => {
    const originalKey = element.searchField.split('.')[1] || element.searchField;
    console.log('originalKey', originalKey)
    if (optionalGroupIds[originalKey]) {
      element.groupId = optionalGroupIds[originalKey];
      console.log(`Updated groupId for ${element.searchField} to ${element.groupId}`);
    }
  });

  console.log('Final transformed object:', transformed);

   // Apply the key renaming just before returning
   transformed.filterElements = transformed.filterElements.map(renameKeys);

  return transformed;
};





export const createStructuredQuery = (input) => {
  console.log("Input Received:", input.KeyWordsearch);
  const regex = /\(([^)]+)\) AND (\w+)/;
  const matches = input.KeyWordsearch.match(regex);

  if (!matches) {
    console.error('No matches found. Check the regular expression.');
    return null;
  }

  if (matches.length !== 3) {
    console.error('Unexpected number of matches. Expected 3 matches, found:', matches.length);
    return null;
  }

  // Extract terms from the matched groups
  const orTermsString = matches[1];
  const andTerm = matches[2];
  const orTerms = orTermsString.split(' or ');

  if (orTerms.length !== 2) {
    console.error('Invalid OR format. Expected two terms separated by " or "');
    return null;
  }

  const [term1, term2] = orTerms.map(term => term.trim());
  //console.log("Parsed Terms:", term1, term2, andTerm);

 
 
  const structuredQuery = {
    pageNumber: 1,
    pageSize: 20,
    filterElements: [
      {
        searchField: "KeyWordsearch", 
        searchOperator: "=",
        searchValue: term1,
        groupId: 1
      },
      {
        searchField: "KeyWordsearch",
        searchOperator: "=",
        searchValue: term2,
        groupId: 1
      },
      {
        searchField: "KeyWordsearch", 
        searchOperator: "=",
        searchValue: andTerm,
        groupId: 2
      }
    ],
    filterGroups: [
      {
        groupId: 1,
        groupOperator: 2, // OR operator
        parentGroupId: 3
      },
      {
        groupId: 2,
        groupOperator: 1, // AND operator, but single condition in this group
        parentGroupId: 3
      },
      {
        groupId: 3,
        groupOperator: 1, // AND operator for combining groups 1 and 2
        parentGroupId: null
      }
    ],
    logicalSearchOperator: 1 // Overall query logic
  };

  return structuredQuery;
}


